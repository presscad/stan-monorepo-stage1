dogsdat <- list(Ndogs = 30, 
                Ntrials = 25,
                Y =  structure(dogsdat_y, .Dim = c(30, 25))); 

dogsstan <- "
data {
  int(0,) Ndogs;
  int(0,) Ntrials;
  int Y[Ndogs, Ntrials];
}

transformed data {
  int xa[Ndogs, Ntrials];
  int xs[Ndogs, Ntrials];
  for (i in 1:Ndogs) {
    for (j in 2 : Ntrials) {
      xs[i, j] <- 0;
      for (k in 1:(j - 1)) xa[i, j] <- xa[i, j] + Y[i, k];
      xs[i, j] <- j - 1 - xa[i, j];
    }
  }
}

parameters {
  real(, -0.00001) alpha;
  real(, -0.00001) beta;
}

transformed parameters {
  real p[2];
  real q[2, 2];
  p[1] <- alpha;
  p[2] <- beta;
  q[1, 1] <- beta;
  q[2, 2] <- alpha;
  q[1, 2] <- 0;
  q[2, 1] <- 1;
} 



model {
  alpha ~ normal(0.0, 316.0);
  beta  ~ normal(0.0, 316.0);
  for(i in 1:Ndogs)
    for (j in 2:Ntrials)
      1 - Y[i, j] ~ bernoulli(exp(alpha * xa[i, j] + beta * xs[i, j]));
}
" 


model_name <- "dogs"; 
dogsrr <- stan.model(model.code = dogsstan, model.name = model_name, 
                     verbose = TRUE) 

